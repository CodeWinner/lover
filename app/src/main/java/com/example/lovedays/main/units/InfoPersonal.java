package com.example.lovedays.main.units;

public class InfoPersonal {
    private String id;
    private String name;
    private String birthDay;
    private String sex;
    private String image;
    private String colorBoderCode;
    private String colorText;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBirthDay() {
        return birthDay;
    }

    public void setBirthDay(String birthDay) {
        this.birthDay = birthDay;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getColorBoderCode() {
        return colorBoderCode;
    }

    public void setColorBoderCode(String colorBoderCode) {
        this.colorBoderCode = colorBoderCode;
    }

    public String getColorText() {
        return colorText;
    }

    public void setColorText(String colorText) {
        this.colorText = colorText;
    }
}
