package com.example.lovedays.main.units;

public class InfoApp {
    private String startDay;
    private String theme;
    private String background;
    private String loveText;
    private String music;
    private String rated;
    private String notifaction;
    private String logoImage;

    public String getStartDay() {
        return startDay;
    }

    public void setStartDay(String startDay) {
        this.startDay = startDay;
    }

    public String getTheme() {
        return theme;
    }

    public void setTheme(String theme) {
        this.theme = theme;
    }

    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getLoveText() {
        return loveText;
    }

    public void setLoveText(String loveText) {
        this.loveText = loveText;
    }

    public String getMusic() {
        return music;
    }

    public void setMusic(String music) {
        this.music = music;
    }

    public String getRated() {
        return rated;
    }

    public void setRated(String rated) {
        this.rated = rated;
    }

    public String getNotifaction() {
        return notifaction;
    }

    public void setNotifaction(String notifaction) {
        this.notifaction = notifaction;
    }

    public String getLogoImage() {
        return logoImage;
    }

    public void setLogoImage(String logoImage) {
        this.logoImage = logoImage;
    }
}
